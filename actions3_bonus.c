#include "push_swap.h"

void	rrr(t_ps *ps)
{
	if (*ps->a_stack)
	{
		*ps->a_stack = (*ps->a_stack)->prev;
	}
	if (*ps->b_stack)
	{
		*ps->b_stack = (*ps->b_stack)->prev;
	}
}
